#**********   Comparison Operators  *************

'''
These operators compare the values on either sides of them....

and decide the relation among them.

They are also called Relational operators.

'''

#*************   Show Example  **********

a = 10

b = 20

print('Exampple of Comparison Operators :\n')

print('10 == 20 : ',a == b) # two operands are equal, then the condition becomes true

print('10 != 20 : ' ,a!=b) # two operands are not equal, then condition becomes true

#  > : left operand is greater than the value of right operand, then condition becomes true

print('10 > 20  : ', a > b)

# < : left operand is less than the value of right operand, then condition becomes true

print('10 < 20  : ', a < b)

# a >= b : left operand is greater than or equal to the value of right operand,
# then condition becomes true

print('10 >= 20 : ', a >= b)

# a <= b : left operand is less than or equal to the value of right operand
# then condition becomes true

print('10 <= 20 : ', a <= b)

print('\n')

