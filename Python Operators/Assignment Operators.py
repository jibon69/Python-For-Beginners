
#**********          Assignment Operators      **********************

# Assign value from variable

# Example of Assignment Operators

a = 10

b = 5


c = a + b # c = a + b assigns value of a + b into c

print(c)


#**********  += Add AND ***********

c += a  # c += a is equivalent to c = c + a

print('15 += 10  : ',c)


#**********   -= Subtract AND  *********

c -= a  # c -= a is equivalent to c = c - a

print('10 -= 5   : ',c)


#**********        *= Multiply AND     ************

c *= a  # c *= a is equivalent to c = c * a

print('15 *= 10  : ',c)


#******************   /= Divide AND  ****************

c /= a  # c /= a is equivalent to c = c / a

print('15 /= 10  : ',c)


#*****************    %= Modulus AND  **************

c %= a  # c %= a is equivalent to c = c % a

print('15 %= 10  : ',c)


#----------         **= Exponent AND  -------------------

c **= a  # c **= a is equivalent to c = c ** a

print('15 **= 10 : ',c)


#-----------    //= Floor Division  ---------------

c //= a  # c //= a is equivalent to c = c ** a

print('15 //= 10 : ',c)



