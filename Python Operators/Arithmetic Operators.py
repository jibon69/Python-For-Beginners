#*****************   Python Arithmetic Operators  ***************

#  Show Example :

#*********   + Addition  ************

a = 10 ; b = 20

c = a + b #  Addition

print('Addition 10+20  = ' , c)

#*********  - Subtraction  *************

a = 10 ; b = 20

c = a - b #  Subtraction

print('Subtraction 10-20  = ' ,c)


#**********   * Multiplication   *************

a = 10 ; b = 20

c = a * b #  Multiplication

print('Multiplication 10*20 = ' ,c)


#***************   / Division   ****************

a = 3 ; b = 10

c = a / b #  Division

print('Division 3 / 10 = ' ,c)


#*************    % Modulus   ***********

#  Modulus returns remainder

a = 3 ; b = 10

c = a % b #  Modulus

print('Modulus 3 % 10  = ' ,c)


#********    ** Exponent  ************

# declear Exponent a ** b # Must be **

# ( power ) calculation on operators

a = 3 ; b = 5

c = a ** b #  Exponent

print('Exponent 3 ** 5  = ' ,c)


#************  Floor Division //  *************

# Floor Division - The division of operands where the result is the quotient

# Example of Floor Division


a = 2 // 5    #  EFloor Division

b = 9//2      #  EFloor Division

c = 9.0//2.0   #  EFloor Division

d = -11//3    #  EFloor Division

e = -11.0//3  #  EFloor Division

print('Floor Division  2 // 5   = ' ,a)
print('Floor Division    9//2   = ' ,b)
print('Floor Division 9.0//2.0  = ' ,c)
print('Floor Division -11//3    = ' ,d)
print('Floor Division -11.0//3  = ' ,e)



